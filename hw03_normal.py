from random import randint


def sort_to_max(a):
    k = ub = len(a)-1
    lb = 1
    while lb < ub:
        for j in range(ub, lb-1, -1):
            if a[j - 1] > a[j]:
                a[j - 1], a[j] = a[j], a[j - 1]
                k = j
            lb = k
        for j in range(lb, ub + 1):
            if a[j - 1] > a[j]:
                a[j - 1], a[j] = a[j], a[j-1]
                k = j
            ub = k
    return a

