# Первыми элементами ряда считать цифры 1 1

def fibonacci(n, m):
    fib = []
    a, b = 0, 1
    for num in range(m):
        fib.append(b)
        a, b = b, a + b
    n -= 1
    res = [fib[i] for i in range(n, m)]
    del fib
    print(res)
    return res


